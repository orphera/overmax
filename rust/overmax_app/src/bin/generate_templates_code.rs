use std::fs;
use std::path::Path;
use image::GenericImageView;

struct RawTemplate {
    label: String,
    char_val: char,
    width: usize,
    height: usize,
    mask: Vec<u8>,
}

fn main() {
    let digits_dir = Path::new("scratch/screenshots/digits");
    if !digits_dir.exists() {
        println!("No digits directory found. Please run collect_templates first.");
        return;
    }
    
    // 문자별 대표 파일 매핑 (가장 선명하고 큰 파일로 대표 수집)
    let candidates = [
        ("0", '0', "0_char_20260701124615_1_4_78.png"),
        ("1", '1', "1_char_20260701011933_1_0_5.png"),
        ("2", '2', "2_char_20260701164242_1_4_80.png"),
        ("3", '3', "3_char_20260701124303_1_4_79.png"),
        ("4", '4', "4_char_20260701124917_1_4_77.png"),
        ("5", '5', "5_char_20260702233310_1_3_61.png"),
        ("6", '6', "6_char_20260701164800_1_3_61.png"),
        ("7", '7', "7_char_20260701165356_1_3_62.png"),
        ("8", '8', "8_char_20260701164242_1_3_61.png"),
        ("9", '9', "9_char_20260701164242_1_0_16.png"),
        ("dot", '.', "dot_char_20260701164242_1_2_53.png"),
        ("percent", '%', "percent_char_20260701173729_1_5_61.png"),
    ];
    
    let mut raw_templates = Vec::new();
    let target_height = 32usize;
    
    for &(label, char_val, filename) in &candidates {
        let path = digits_dir.join(filename);
        if !path.exists() {
            println!("Warning: Template file '{}' missing. Skipping.", filename);
            continue;
        }
        
        let img = image::open(&path).expect("Failed to open template image");
        let (w, h) = img.dimensions();
        
        // 32px 높이로 세로 정규화 (가로 비율은 그대로 유지하며 리사이징)
        let target_width = ((w as f32 * target_height as f32 / h as f32).round()) as usize;
        
        let resized = img.resize_exact(target_width as u32, target_height as u32, image::imageops::FilterType::Lanczos3);
        let gray = resized.to_luma8();
        let mut mask = Vec::new();
        for y in 0..target_height {
            for x in 0..target_width {
                let pixel = gray.get_pixel(x as u32, y as u32)[0];
                mask.push(if pixel >= 128 { 1 } else { 0 });
            }
        }
        
        raw_templates.push(RawTemplate {
            label: label.to_string(),
            char_val,
            width: target_width,
            height: target_height,
            mask,
        });
        
        println!("Processed template '{}' -> size {}x{}", label, target_width, target_height);
    }
    
    // Rust 소스코드 덤프 생성
    let mut code = String::new();
    code.push_str("// This file is auto-generated by generate_templates_code binary.\n");
    code.push_str("// Do not edit manually.\n\n");
    code.push_str("pub struct FontTemplate {\n");
    code.push_str("    pub char_val: char,\n");
    code.push_str("    pub width: usize,\n");
    code.push_str("    pub height: usize,\n");
    code.push_str("    pub mask: &'static [u8],\n");
    code.push_str("}\n\n");
    
    for t in &raw_templates {
        code.push_str(&format!("// Template for '{}'\n", t.char_val));
        code.push_str(&format!("const TEMPLATE_MASK_{}: [u8; {}] = [\n", t.label.to_uppercase(), t.mask.len()));
        for y in 0..t.height {
            code.push_str("    ");
            for x in 0..t.width {
                code.push_str(&format!("{}, ", t.mask[y * t.width + x]));
            }
            code.push_str("\n");
        }
        code.push_str("];\n\n");
    }
    
    code.push_str("pub const DIGIT_TEMPLATES: [FontTemplate; ");
    code.push_str(&format!("{}] = [\n", raw_templates.len()));
    for t in &raw_templates {
        code.push_str("    FontTemplate {\n");
        code.push_str(&format!("        char_val: {:?},\n", t.char_val));
        code.push_str(&format!("        width: {},\n", t.width));
        code.push_str(&format!("        height: {},\n", t.height));
        code.push_str(&format!("        mask: &TEMPLATE_MASK_{},\n", t.label.to_uppercase()));
        code.push_str("    },\n");
    }
    code.push_str("];\n");
    
    let out_file_path = Path::new("rust/overmax_app/src/detector/templates/digit.rs");
    fs::write(out_file_path, code).expect("Failed to write detector/templates/digit.rs");
    println!("Successfully generated Rust templates source in {:?}", out_file_path);
}
